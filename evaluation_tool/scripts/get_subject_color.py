def get_subject_color(subject):
    if subject == "Evangelische Religion" or subject == "Musik":
        return "#DC692A"
    if subject == "Deutsch" or subject == "Englisch" or subject == "Latein":
        return "#8F1D4A"
    if subject == "Mathematik":
        return "#449879"
    if subject == "Allgemeine Lehrevaluation":
        return "#535353"
